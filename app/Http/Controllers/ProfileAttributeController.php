<?php

namespace App\Http\Controllers;

use App\Models\ProfileAttribute;
use Illuminate\Http\Request;
use Illuminate\Support\Facades\DB;

class ProfileAttributeController extends Controller
{
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $profileAttributes = DB::table('profile_attributes')
            ->join('interests', 'profile_attributes.id', '=', 'interests.profile_attributes_id')
            ->get();
        return $profileAttributes;
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'profile_id' => 'required'
        ]);

        return ProfileAttribute::create($request->all());
    }

    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        return ProfileAttribute::find($id);
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $profileAttribute = ProfileAttribute::find($id);
        $profileAttribute->update($request->all());
        return  $profileAttribute;
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        return ProfileAttribute::destroy($id);
    }

